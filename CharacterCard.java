public class CharacterCard {
    public static void main(String[] args) {
        String name = "Aragon";
        String characterClass = "warrior";
        int level = 15;
        int health = 100;
        double gold = 250.75;
        char rank = "S";

        System.out.println("----Character card----");
        System.out.println("Name: " + name);
        System.out.println("Character Class: " + characterClass);
        System.out.println("Level: " + level);
        System.out.println("Health: " + health);
        System.out.println("Coin reserve: " + gold);
        System.out.println("Character rank: " + rank);

    }
}